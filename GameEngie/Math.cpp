//==========================================================================================================//
// NAME: FLOAT GetVectorRotation(D3DXVECTOR3 vec)
// DESC: 数学用関数 回転量計算
//==========================================================================================================//
FLOAT GetVectorRotation(D3DXVECTOR3 vec)
{
	FLOAT Angle;
	Angle = atan2f(vec.x, vec.z);
	return Angle;
}

//==========================================================================================================//
// NAME: FLOAT GetVectorRotation(D3DXVECTOR3 vec)
// DESC: 数学用関数 回転角度計算
//==========================================================================================================//
FLOAT GetVectorRotationAngel(D3DXVECTOR3 vec)
{
	FLOAT Angle;
	Angle = atan2f(vec.x, vec.z);
	return D3DXToDegree(Angle);
}